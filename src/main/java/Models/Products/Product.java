package Models.Products;

public class Product {
    private String name;
    private long price;
    private String img;
    private String taste;

    public Product() {
    }

    public String getName() {
        return name;
    }

    public long getPrice() {
        return price;
    }

    public String getImg() {
        return img;
    }

    public String getTaste() {
        return taste;
    }
}
